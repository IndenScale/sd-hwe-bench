#!/usr/bin/env bash
# Assemble sections/*.md into a single manuscript file.
# Usage: ./scripts/assemble.sh [output.md]
# Default output: manuscript.md (in the paper root)
#
# Heading hierarchy in assembled output:
#   h1 = paper title (reserved, not used)
#   h2 = section title (e.g., "1. 引言")
#   h3 = subsection (original ##)
#   h4 = sub-subsection (original ###)

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PAPER_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
SECTIONS_DIR="$PAPER_DIR/sections"
OUTPUT="${1:-$PAPER_DIR/manuscript.md}"

FILES=(
    "00-header.md"
    "00-abstract.md"
    "01-introduction.md"
    "02-background.md"
    "03-the-eac-approach.md"
    "04-adl.md"
    "05-esa.md"
    "06-evaluation.md"
    "07-related-work.md"
    "08-conclusion.md"
)

python3 - "$SECTIONS_DIR" "$OUTPUT" "${FILES[@]}" << 'PYEOF'
import sys, os, re

sections_dir, output = sys.argv[1], sys.argv[2]
files = sys.argv[3:]

def promote(line: str) -> str:
    """Promote a markdown heading by exactly 1 level."""
    m = re.match(r'^(#{1,6})\s', line)
    if not m:
        return line
    level = len(m.group(1))
    if level >= 6:
        return line  # max ######, can't promote
    new_hashes = '#' * (level + 1)
    return new_hashes + line[level:]

result = ['<!-- Generated by scripts/assemble.sh -->',
          '<!-- DO NOT EDIT THIS FILE — edit sections/*.md instead -->',
          '']

for fname in files:
    path = os.path.join(sections_dir, fname)
    if not os.path.isfile(path):
        sys.exit(f'ERROR: missing {path}')

    with open(path) as f:
        lines = f.readlines()

    # First line is the file's own h1
    if not lines or not lines[0].startswith('# '):
        sys.exit(f'ERROR: {path} must start with "# Title"')
    section_title = lines[0][2:].strip()

    result.append(f'## {section_title}')
    result.append('')

    # Promote remaining headings by 1 level
    for line in lines[1:]:
        result.append(promote(line).rstrip('\n'))

    result.append('')
    result.append('')

with open(output, 'w') as f:
    f.write('\n'.join(result))

total_lines = sum(1 for l in result if l.strip())
print(f'Assembled manuscript → {output}')
print(f'  Sections: {len(files)}')
print(f'  Lines:    {total_lines}')
PYEOF
