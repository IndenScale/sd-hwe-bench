#!/usr/bin/env bash
# Assemble sections-en/*.md into an English manuscript.
# Usage: ./scripts/assemble-en.sh [output.md]
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PAPER_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
SECTIONS_DIR="$PAPER_DIR/sections-en"
OUTPUT="${1:-$PAPER_DIR/manuscript-en.md}"

FILES=(
    "00-header.md"
    "00-abstract.md"
    "01-introduction.md"
    "02-background.md"
    "03-the-eac-approach.md"
    "04-adl.md"
    "05-esa.md"
    "06-evaluation.md"
    "07-related-work.md"
    "08-conclusion.md"
)

python3 - "$SECTIONS_DIR" "$OUTPUT" "${FILES[@]}" << 'PYEOF'
import sys, os, re

sections_dir, output = sys.argv[1], sys.argv[2]
files = sys.argv[3:]

def promote(line: str) -> str:
    m = re.match(r'^(#{1,6})\s', line)
    if not m:
        return line
    level = len(m.group(1))
    if level >= 6:
        return line
    return '#' * (level + 1) + line[level:]

result = ['<!-- Generated by scripts/assemble-en.sh -->',
          '<!-- DO NOT EDIT THIS FILE — edit sections-en/*.md instead -->',
          '']

for fname in files:
    path = os.path.join(sections_dir, fname)
    if not os.path.isfile(path):
        sys.exit(f'ERROR: missing {path}')
    with open(path) as f:
        lines = f.readlines()
    if not lines or not lines[0].startswith('# '):
        sys.exit(f'ERROR: {path} must start with "# Title"')
    section_title = lines[0][2:].strip()
    result.append(f'## {section_title}')
    result.append('')
    for line in lines[1:]:
        result.append(promote(line).rstrip('\n'))
    result.append('')
    result.append('')

with open(output, 'w') as f:
    f.write('\n'.join(result))

total_lines = sum(1 for l in result if l.strip())
print(f'Assembled manuscript → {output}')
print(f'  Sections: {len(files)}')
print(f'  Lines:    {total_lines}')
PYEOF
